// three true values example

let a = true;  // direct assignment of a boolean value
let b = 1;   // any non-zero number is considered truthy
let c = "hello";  // non-empty strings are considered truthy


// three false values example

let a = false; // direct assignment of a boolean value
let b = 0; // 0 is considered falsy
let c = "";  // empty strings are considered falsy
